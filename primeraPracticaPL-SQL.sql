DECLARE
MINIMO NUMBER;

BEGIN
	SELECT MIN(TAMANO) INTO MINIMO FROM EJEMPLARESMARIPOSAS;	

	IF MINIMO > 50 THEN
		DBMS_OUTPUT.PUT_LINE('Es mayor que 50.');
	ELSE
		DBMS_OUTPUT.PUT_LINE('Es menor que 50.');
	END IF;

END;


DECLARE 
NOTA NUMBER;

BEGIN

	NOTA := -4;

	IF NOTA = 10 OR NOTA = 9 THEN 
		DBMS_OUTPUT.PUT_LINE('Sobresaliente');
	ELSIF NOTA = 8 OR NOTA = 7 THEN 
		DBMS_OUTPUT.PUT_LINE('Notable');
	ELSIF NOTA = 6 THEN
		DBMS_OUTPUT.PUT_LINE('Bien');
	ELSIF NOTA = 5 THEN 
		DBMS_OUTPUT.PUT_LINE('Suficiente');
	ELSIF NOTA < 5 AND NOTA >= 0THEN 
		DBMS_OUTPUT.PUT_LINE('Insuficiente');
	ELSE
		DBMS_OUTPUT.PUT_LINE('Nota no v√°lida');
	END IF;

END;


